package com.java.ex01;

public class Ex06 {

	public static void main(String[] args) {
	
		int a = 7 ;
		int b = 3 ;
		
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		
		System.out.println(a/b); //몫
		System.out.println(a%b); //나머지--> 짝수 홀수, 순환패턴 만들때 자주씀


		System.out.println(a);        
        System.out.println(++a);     // 더하고 나서 더한값을 대입 또는 출력  
        System.out.println(a++);    // 출력 또는 대입부터 하고 그다음 더한값을 데이터에 남김 
        System.out.println(a);     // a++에서 계산된 값이 출력됨 
		
	}

}
