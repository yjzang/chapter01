package com.java.ex01;

public class Ex03 {

	public static void main(String[] args) {
		
		
		boolean b1 = true ;
		boolean b2 = false ; 
		
		// true 와 false는 문자열이 아니라 그 자체로 의미를 갖는 예약어이다.
		
		System.out.println(b1);
		System.out.println(b2);
		
		
		int a = 3 ;
		int b = 5 ;
		boolean result = a > b ;   //boolean형으로 선언된 변수에는 수식이 들어갈 수 있다.
		
		System.out.println(result);
		
		
	}
}
