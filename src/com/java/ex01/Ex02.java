package com.java.ex01;

public class Ex02 {

	
	public static void main(String[] args) {
		
		double var01= 3.14;
		float var02= 3.14f;
		
		System.out.println(var01);
		System.out.println(var02);
		
		double var03 = 0.12345678901234567 ;
		float var04 = 0.123456789012345678f ;
		
		System.out.println(var03);
		System.out.println(var04);
		
		// 소수점 아래자리로 내려가면 정확한 값이 출력되지는 않는다.

        //float은 숫자뒤에 f를 , long형은 L을 붙여준다. 
		
		// int의 범위를 넘는 long일때 L을 붙인다. 그러나 불안하면 그냥 long형에는 다 L을 붙여라  
	         
	}
}
